function ll_get_img_url ( vol, page) {
	var vol_page = ll_validate_vol_page( vol, page);
	var p = vol_page[1];
	var prefixed_page = p;
	if ( p < 10) prefixed_page = "00" + p;
	else if ( p < 100) prefixed_page = "0" + p;
	var img_url = "ll/" + vol_page[0] + "/ll-v" + vol_page[0] + "-p" + prefixed_page + ".png";
	//debug( img_url);
	return img_url;
}

function ll_validate_vol_page( volume, pageno) {
	// Detect wrap around, if page 405 of vol 1 is requested
	// return vol 2, page 1
	var retval = [];
	retval[0] = volume;
	retval[1] = pageno;
	if ( volume < 1 ) retval[0] = 1;
	if ( volume > 8 ) retval[0] = 8;
	if ( pageno > ll_volumes[volume-1]) {
		// Assume it will only wrap once due to prev/next page navigation
		if ( volume == 8) { retval[1] = ll_volumes[volume-1]; }
		else {
			retval[0] = volume + 1;
			retval[1] = pageno - ll_volumes[volume-1];
		}
	}
	if ( pageno < 1 ) {
		if ( volume == 1 ) pageno = 1;
		else { 
			retval[0] = volume - 1;
			retval[1] = pageno + ll_volumes[volume-2];
		}
	}
	cur_ll_vol = retval[0];
	cur_ll_page = retval[1];
	return retval;
}

function get_ll_page( volume, pageno) {
	var running_total = 0;
	for(var i = 0; i < ll_volumes.length; i++) {
		if ( volume == (i + 1) ) {
			running_total += pageno;
			break;
		}
		running_total += ll_volumes[i];
	}
	return running_total;
}

function get_ll_volume_page( pageno) {
	var volume = 1;
	var running_total = 0;
	var retval = [ 0, 0];
	for(var i = 0; i < ll_volumes.length; i++) {
	  //var yourObject = volumes[i];
		var last_running_total = running_total;
		running_total += ll_volumes[i];
		if ( running_total > pageno ) {
			volume_page = pageno - last_running_total;
			retval = [ volume, volume_page];
			break
		}
		//debug( [pageno, running_total]);
		volume++;
	}
	//debug( retval);
	return retval;
}

